    public static String numberTranscribe(int num) {
        String letter = "";
        
        if(num == 1) {
            letter = "A";
        } else if (num == 2) {
            letter = "B";
        } else if (num == 3) {
            letter = "C";
        } else if (num == 4) {
            letter = "D";
        } else if (num == 5) {
            letter = "E";
        } 
        
        return letter;
    }
           
